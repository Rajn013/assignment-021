# Q1. What is a probability distribution, exactly? If the values are meant to be random, how can you predict them at all?
# 

# A probability distribution tells us how likely different outcomes are in a random event. While we can't predict specific values, the distribution helps us understand the overall chances and patterns of those values. It allows us to make predictions based on probabilities and estimate the possible range of outcomes.

# Q2. Is there a distinction between true random numbers and pseudo-random numbers, if there is one? Why are the latter considered “good enough”?
# 

# True random numbers are genuinely unpredictable, while pseudo-random numbers are generated using algorithms and appear random but are actually predictable. Pseudo-random numbers are considered "good enough" for most purposes because they exhibit statistical randomness, are efficient, and widely applicable.

# Q3. What are the two main factors that influence the behaviour of a "normal" probability distribution?
# 

#  the average (mean) and the spread (standard deviation). The mean determines the center of the distribution, while the standard deviation determines how spread out the values are around the mean.

# Q4. Provide a real-life example of a normal distribution.
# 

# 
# Human heights follow a normal distribution. Most people have heights close to the average, with fewer individuals being very tall or very short. 

# Q5. In the short term, how can you expect a probability distribution to behave? What do you think will happen as the number of trials grows?
# 

# A probability distribution can behave unpredictably. However, as the number of trials increases, the distribution becomes more predictable and aligns with its expected shape and probabilities.

